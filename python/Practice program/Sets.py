
#  Creating a set
set1={'coa','dbms','os','maths'}
print(set1)


#  Operations on sets
set2={'1','2','3','4'}
set3={'2','3','5','7'}

print(set2.union(set3)) # For union of two sets

print(set2.intersection(set3)) # For finding intersection

print(set2.difference(set3)) # For (set2 - set3)
print(set3.difference(set2)) # For (set3 - set2)
